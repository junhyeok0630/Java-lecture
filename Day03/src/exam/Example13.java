package exam;

public class Example13 {
	public static void main(String[] args) {
		/*
		 * 논리 부정 연산자
		 * - boolean 값을 가지는 피연산자 앞에
		 * (!)를 붙혀서 값을 반대로 바꾸는 역할
		 * 
		 * 즉) true => false,
		 *    false => true
		 */
		boolean isHuman = true;
		System.out.println(isHuman);
		System.out.println(!isHuman);
	}
}
